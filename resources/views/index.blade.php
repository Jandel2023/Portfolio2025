            // Blog Modal Logic
            function openModal(id) {
                const modal = document.getElementById('modal-' + id);
                modal.style.display = 'flex';

                // Close blog modal on outside click
                modal.onclick = function (e) {
                    if (e.target === modal) {
                        closeModal(id);
                    }
                };
            }

            function closeModal(id) {
                const modal = document.getElementById('modal-' + id);
                modal.style.display = 'none';
            }
